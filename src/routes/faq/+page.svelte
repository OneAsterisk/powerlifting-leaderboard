            <h2>What is Collegiate Strength?</h2>
            <p>Collegiate Strength is a platform for collegiate powerlifters to submit their best lifts and compare them with other lifters across the country. Our goal is to provide a fun and competitive environment for collegiate lifters to showcase their strength and connect with other lifters.</p>
        </section>
        <section>
            <h2>How can I submit my lifts?</h2>
            <p>To submit your lifts simply sign in with a google account and go to the Submit Lifts page. In there you can enter all your numbers, age, gender, university, and weight.</p>
        </section>
        <section>
            <h2>What lift gets displayed on the leaderboard?</h2>
            <p>Your lift with the highest total gets displayed and ranked on the global leaderboard!</p>
        </section>
        <section>
            <h2>Why use Collegiate Strength?</h2>
            <p>Collegiate strength allows you to submit your own lifts without having to compete to see how you rank among many other collegiate level lifters.
            This allows you to keep your highest total up to date without having to wait to compete or for results to come out from your meet.
            </p>
        </section>
        <section>
            <h2>Why is my dots different than what was at the meet?</h2>
            <p>Our dots score calculation does not take into effect age. If this feature gets requested we can add it and work to retroactively update the leaderboard. However it is currently low on priority.</p>
        </section>
